<h2>9. 画像最適化は検索でより大きな役割を果たす</h2>

<p>視覚的な画像検索は飛躍的に進化しています。以前は画像を見るだけの時代でしたが、これからは画像を使って商品を購入したり、情報を得たりすることができるようになります。今後は、画像を使って商品を購入したり、情報を得たりすることができるようになるでしょう。Googleは画像の適切な表示と最適化を長年にわたって主張してきたのでこれが長期的な計画の一環であることは理にかなっています。</p>

<p>ウェブサイトの画像が最適化されていない場合は今すぐに対処しましょう。高品質で関連性の高い画像を使用しファイル名をカスタマイズして対応するページのコンテンツに関連するように写真ファイルにラベルを付けてください。altタグはクローラーが画像を分類するために使用します。最後にサイトマップに画像を追加しておくとクロールされやすくなります。</p>

<h2>10. 主キーワードに関連する複キーワードの重要性が高まる</h2>

<p>SEOの専門家は以前は目隠しをしているかのように主キーワードに焦点を当てていました。しかし今では複キーワードも同様に重要であることがわかっています。Googleはもはや単語の文字列だけを見ているのではありません。クエリの文脈を分析しユーザーの検索意図を見極めようとしています。つまり、論理的に関連性のある主キーワードと複キーワードを介してより関連性の高い情報を提供すればするほど良いということです。</p>

<h2>11. ローカル検索はSEO戦略においてより大きな役割を果たすだろう</h2>

<p>人々がインターネットといえばそのグローバルな性質を思い浮かべることが多い。実際にはほとんどの人が検索エンジンを使って地域に密着した商品やサービスを探しています。例えば、近所のレストランを探しているかもしれません。ローカルSEOは重要でありそれは進化しています。<strong>この進化は一部のSEOマーケティング担当者が「新常識」と呼んでいるゼロクリック検索の台頭に起因</strong>しています。</p>

<p>ゼロクリック検索ではユーザーのクエリは検索したGoogleページ上に回答されます。そのためユーザーはランキング結果をクリックすることはありません。ゼロクリック検索が増加している理由の一つは特集スニペットの増加が要因なのではないでしょうか。ゼロクリック検索の多くは、"ローカルパック "と呼ばれるもので検索結果上に結果を表示するローカル検索です。</p>


