<h2>6. 注目のスニペットがより目立つようになる</h2>

<p>Googleに何かを入力して検索すると検索結果の上位部に実際の結果の上にボックスが表示されることがあります。それがスニペットです。</p>

<p>スニペットは検索結果の最初のページに表示されるようになるための素晴らしい方法です。さらにスニペットは競合他社から大きなトラフィックを奪います。</p>

<p>注目のスニペットは情報の塊を表示し多くの場合、Q&Aや簡単な箇条書きハウツーガイドとして構成されています。また画像、星付きレビュー、製品価格、および類似の情報を含むリッチスニペットもあります。</p>

<p>狙ってスニペットを表示することは恐ろしく難しい事ですが、競合を出し抜きスニペットを表示できるページが作れればワードによるとは思いますが、かなりのトラフィックが期待できるでしょう。</p>

<h2>7. 未来を読む予測検索</h2>

<p>Google Discoverは2017年に開始されユーザーのクエリすら必要としない新しいタイプの検索を解き放ちました。DiscoverもまたGoogleのAI駆動ツールの1つ。このコンテンツレコメンデーションツールはユーザーの行動パターンを持続的に識別し、その習慣を徐々に学習していきます。この情報をもとにDiscoverはユーザーが最も興味を持つ可能性の高い最も正確なコンテンツを特定することができます。</p>

<p>Google Discoverはすでに8億人以上のアクティブユーザーがいると主張しています。表示されるためには特別なことをする必要はありません。Googleがあなたのページをインデックスした場合そのページは含まれます。コンテンツはコンテンツの品質、ユーザーの関心度を検査するアルゴリズムに基づいてランク付けされます。Googleは正確な要因を伝えていませんが位置情報の履歴、閲覧履歴、アプリの使用状況、カレンダー、検索履歴、自宅や職場の場所などが関連しているようです。</p>


